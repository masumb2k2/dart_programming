void main(){
  int a =34;
  int b=23;
  //arithmetic operator
  print(a+b); //addition
  print(a-b); //subtraction
  print(a*b); //multiplication
  print(a/b); //division
  print(a%b); //modulus'

//unary operator
  print(a++); //postfix -> first print then increment
  print(++a); //prefix -> first increment then print
  print(--a); //pre decrement
  print(a--); //post decrement
  
//relational operator
  print(a>b); //greater than
  print(a<b); //less than
  print(a>=b); //greater than or equal to
  print(a<=b); //less than or equal to
  print(a==b); //equal toqas
  print(a!=b); //not equal to

//logical operator
  print(a<30 && b>30); //true && true = true, 
  print(a<30 || b>30);//true || false = true

//assignment operator
  a *=45; // a=a*45



}